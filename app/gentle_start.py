"""Спокойный вход в курс: одна новая идея на один короткий урок."""

from __future__ import annotations

from typing import Any


def _theory(title: str, text: str, example: str, tip: str = "") -> dict[str, str]:
    return {"title": title, "text": text, "example": example, "tip": tip}


def _choice(
    question_id: str,
    prompt: str,
    options: list[str],
    answer: str,
    explanation: str,
    guide: str,
    badge: str = "",
) -> dict[str, Any]:
    question: dict[str, Any] = {
        "id": question_id,
        "kind": "choice",
        "prompt": prompt,
        "options": options,
        "answer": answer,
        "explanation": explanation,
        "guide": guide,
    }
    if badge:
        question["badge"] = badge
    return question


def _input(
    question_id: str, prompt: str, answers: list[str], explanation: str, guide: str
) -> dict[str, Any]:
    return {
        "id": question_id,
        "kind": "input",
        "prompt": prompt,
        "answers": answers,
        "placeholder": "Короткий ответ",
        "explanation": explanation,
        "guide": guide,
    }


def _code(
    question_id: str,
    prompt: str,
    starter: str,
    expected: str,
    explanation: str,
    hint: str,
    guide: str,
    test_inputs: list[str] | None = None,
) -> dict[str, Any]:
    question: dict[str, Any] = {
        "id": question_id,
        "kind": "code",
        "prompt": prompt,
        "starter": starter,
        "tests": [{"kind": "stdout", "expected": expected}],
        "explanation": explanation,
        "hint": hint,
        "guide": guide,
    }
    if test_inputs:
        question["test_inputs"] = test_inputs
        question["input_example"] = test_inputs
    return question


def _lesson(
    lesson_id: str,
    title: str,
    subtitle: str,
    theory: list[dict[str, str]],
    questions: list[dict[str, Any]],
) -> dict[str, Any]:
    return {
        "id": lesson_id,
        "module_id": "gentle-start",
        "order": 0,
        "title": title,
        "subtitle": subtitle,
        "duration": 4,
        "xp": 12,
        "theory": theory,
        "questions": questions,
    }


