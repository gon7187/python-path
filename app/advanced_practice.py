"""Тематические executable-задачи для 108 уроков расширенного курса.

В отличие от прежней ротации шестнадцати базовых упражнений, каждая запись здесь
проверяет инструмент текущего микроурока. ``solution`` хранится только внутри
авторского реестра: публичная карточка получает заготовку, тесты и постепенные
подсказки, но не готовый код.
"""

from __future__ import annotations

from dataclasses import dataclass
from textwrap import indent
from typing import Any


@dataclass(frozen=True)
class TaskBlueprint:
    """Авторская спецификация одной тематической задачи."""

    prompt: str
    starter: str
    solution: str
    cases: tuple[tuple[str, Any], ...]
    plan: str
    near_solution: str
    required_tokens: tuple[str, ...]


def _function(
    signature: str,
    prompt: str,
    body: str,
    cases: tuple[tuple[str, Any], ...],
    plan: str,
    near_solution: str,
    *required_tokens: str,
    prelude: str = "",
) -> TaskBlueprint:
    """Собрать обычную функцию и компилируемую, но незавершённую заготовку."""
    prefix = f"{prelude.rstrip()}\n\n" if prelude.strip() else ""
    starter = f"{prefix}{signature}\n    # Напиши решение здесь\n    return None\n"
    solution = f"{prefix}{signature}\n{indent(body.rstrip(), '    ')}\n"
    return TaskBlueprint(
        prompt=prompt,
        starter=starter,
        solution=solution,
        cases=cases,
        plan=plan,
        near_solution=near_solution,
        required_tokens=required_tokens,
    )


def _raw(
    prompt: str,
    starter: str,
    solution: str,
    cases: tuple[tuple[str, Any], ...],
    plan: str,
    near_solution: str,
    *required_tokens: str,
) -> TaskBlueprint:
    """Собрать задачу с классом, декоратором или несколькими функциями."""
    return TaskBlueprint(
        prompt=prompt,
        starter=starter.rstrip() + "\n",
        solution=solution.rstrip() + "\n",
        cases=cases,
        plan=plan,
        near_solution=near_solution,
        required_tokens=required_tokens,
    )


