def topical_task_blueprint(module_id: str, slug: str) -> TaskBlueprint:
    """Вернуть внутреннюю спецификацию; используется только проверками курса."""
    return TOPICAL_TASKS[(module_id, slug)]


def build_topical_code_task(
    module_id: str,
    lesson_spec: tuple[str, str, str, str, str, str, str],
) -> dict[str, Any]:
    """Собрать публичную code-задачу с тематическими опорами без готового ответа."""
    slug, title, subtitle, keyword, example, concept, _ = lesson_spec
    blueprint = topical_task_blueprint(module_id, slug)
    signature = next((line.strip() for line in example.splitlines() if line.strip()), keyword)
    return {
        "prompt": f"Тематическая практика «{title}». {blueprint.prompt}",
        "starter": blueprint.starter,
        "tests": [
            *(
                {"kind": "call", "call": call, "expected": expected}
                for call, expected in blueprint.cases
            ),
            {
                "kind": "source",
                "required_tokens": list(blueprint.required_tokens),
                "expected": True,
            },
        ],
        "explanation": (
            f"Задача проверяет именно тему «{title}»: {concept} "
            "Попробуй придумать ещё один вход и предсказать результат без запуска."
        ),
        "guide": f"Тема: {keyword}. {blueprint.plan}",
        "hint": blueprint.near_solution,
        "hints": [
            "Сначала назови входные данные и требуемый результат своими словами. "
            "Затем выбери одно действие, которое превращает первое во второе.",
            f"Нужный инструмент — «{keyword}». {concept}",
            f"План решения: {blueprint.plan}",
            f"Почти решение: {blueprint.near_solution}",
        ],
        "tool_help": [
            {
                "name": keyword,
                "kind": "Инструмент текущего урока",
                "signature": signature,
                "description": f"{concept} {subtitle}.",
                "example": example,
                "language": (
                    "shell"
                    if module_id == "quality" and slug in {"format-lint", "git"}
                    else "python"
                ),
            }
        ],
        "purpose": "topical_application",
        "focus_concepts": [keyword],
        "review_concepts": [],
        "mandatory": True,
        "required_tokens": list(blueprint.required_tokens),
        "reference_solution": blueprint.solution,
    }
