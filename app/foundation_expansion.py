"""Плавные микроуроки для фундаментальной части курса.

Модуль намеренно не импортирует :mod:`app.content`: все элементы остаются
обычными словарями, поэтому основной учебный план может безопасно применить
замены и вставки во время сборки курса.
"""


def _card(
    title: str,
    text: str,
    example: str,
    output: str,
    tip: str = "",
) -> dict:
    card = {
        "title": title,
        "text": text,
        "example": example,
        "language": "python",
        "output": output,
        "tip": tip,
    }
    return card


def _choice(
    question_id: str,
    prompt: str,
    options: list[str],
    answer: str,
    explanation: str,
    guide: str,
) -> dict:
    return {
        "id": question_id,
        "kind": "choice",
        "prompt": prompt,
        "options": options,
        "answer": answer,
        "explanation": explanation,
        "guide": guide,
    }


def _input(
    question_id: str,
    prompt: str,
    answers: list[str],
    explanation: str,
    guide: str,
) -> dict:
    return {
        "id": question_id,
        "kind": "input",
        "prompt": prompt,
        "answers": answers,
        "placeholder": "Короткий ответ",
        "explanation": explanation,
        "guide": guide,
    }


def _code(
    question_id: str,
    prompt: str,
    starter: str,
    tests: list[dict],
    explanation: str,
    guide: str,
    hint: str,
) -> dict:
    return {
        "id": question_id,
        "kind": "code",
        "prompt": prompt,
        "starter": starter,
        "tests": tests,
        "explanation": explanation,
        "guide": guide,
        "hint": hint,
        "mandatory": True,
    }


def _lesson(
    lesson_id: str,
    module_id: str,
    title: str,
    subtitle: str,
    theory: list[dict],
    questions: list[dict],
    duration: int = 9,
    xp: int = 30,
) -> dict:
    return {
        "id": lesson_id,
        "module_id": module_id,
        "order": 0,
        "title": title,
        "subtitle": subtitle,
        "duration": duration,
        "xp": xp,
        "theory": theory,
        "questions": questions,
    }


