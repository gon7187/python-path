def _challenge(
    lesson_id: str,
    prompt: str,
    starter: str,
    tests: list[dict],
    explanation: str,
    guide: str,
    hint: str,
    test_inputs: list[str] | None = None,
) -> dict:
    question = _code(
        f"challenge-{lesson_id}",
        prompt,
        starter,
        tests,
        explanation,
        guide,
        hint,
    )
    question.update(
        {
            "purpose": "cumulative_transfer",
            "scaffold_level": "independent",
            "badge": "🔁 Вспомни и соедини",
        }
    )
    if test_inputs is not None:
        question["test_inputs"] = test_inputs
        question["input_example"] = test_inputs
    return question


