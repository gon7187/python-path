def _theory(
    title: str,
    text: str,
    example: str,
    tip: str = "",
    language: str = "python",
    output: str | None = None,
    example_status: str = "fragment",
) -> dict[str, Any]:
    """Собрать карточку теории и явно пометить язык примера."""
    card: dict[str, Any] = {
        "title": title,
        "text": text,
        "example": example,
        "tip": tip,
        "language": language,
        "example_status": example_status,
    }
    if output is not None:
        card["output"] = output
    return card


